			<h1>Política de Reembolso</h1>
			<p><strong>Data de Vigência:</strong> 10 de dezembro de 2025</p>

			<h2>1. Introdução</h2>
			<p>
				Nosso objetivo é fornecer um processo de reembolso claro, justo e transparente. Esta Política de Reembolso explica as condições sob as quais os reembolsos podem ser emitidos pelos serviços adquiridos através de nossa plataforma.
			</p>

			<h2>2. Direito de Solicitar Reembolso</h2>
			<ul>
				<li>Você pode solicitar um reembolso dentro de 14 dias após fazer seu pedido.</li>
				<li>As solicitações de reembolso devem ser enviadas entrando em contato conosco em <a href="mailto:support@3i-visa.org">support@3i-visa.org</a>.</li>
			</ul>

			<h2>3. Elegibilidade para Reembolso</h2>
			<p>Os reembolsos podem ser concedidos nas seguintes condições:</p>

			<h3>Reembolso Total – Antes do Início do Processamento</h3>
			<p>
				Se você cancelar sua solicitação antes que nossa equipe comece o processamento, você é elegível para um reembolso total da taxa de serviço.
			</p>

			<h3>Reembolso Parcial – Após o Início do Processamento</h3>
			<p>
				Se o processamento já tiver começado, um reembolso parcial de 50% pode ser emitido quando a solicitação não puder ser concluída devido a fatores fora do seu controle. Se sua solicitação precisar de correções ou informações adicionais, reprocessaremos sua solicitação sem custo adicional em vez de emitir um reembolso.
			</p>

			<h3>Sem Reembolso – Após o Processamento Concluído</h3>
			<p>
				Uma vez que sua solicitação tenha sido concluída e a documentação relevante tenha sido entregue a você, os reembolsos não podem ser fornecidos, pois o serviço já foi cumprido.
			</p>

			<h2>4. Tempo de Processamento do Reembolso</h2>
			<p>
				Os reembolsos aprovados serão emitidos para o método de pagamento original dentro de 5 a 10 dias úteis, dependendo da sua instituição financeira.
			</p>

			<h2>5. Situações Não Reembolsáveis</h2>
			<p>Os reembolsos não serão concedidos nas seguintes situações:</p>
			<ul>
				<li>O serviço foi totalmente concluído e entregue.</li>
				<li>Atrasos ou decisões tomadas por autoridades governamentais ou de imigração.</li>
				<li>Falha em fornecer informações precisas ou documentação necessária.</li>
			</ul>

			<h2>6. Disputas e Prevenção de Estorno</h2>
			<p>
				Se você encontrar algum problema com seu pedido, por favor entre em contato conosco primeiro em
				<a href="mailto:support@3i-visa.org">support@3i-visa.org</a>
				antes de solicitar um estorno. Muitos problemas podem ser resolvidos rapidamente com nossa equipe de suporte.
			</p>

			<h2>7. Alterações a Esta Política</h2>
			<p>
				Podemos atualizar esta Política de Reembolso a qualquer momento. Quaisquer modificações entram em vigor imediatamente após serem publicadas nesta página.
			</p>

