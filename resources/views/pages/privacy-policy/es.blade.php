<h2>3. Qué Datos Recopilamos</h2>
<p>Dependiendo de su interacción con nuestro sitio web, podemos recopilar los siguientes datos personales:</p>
<ul>
	<li><strong>Información de Identidad:</strong> Nombre, dirección de correo electrónico, número de teléfono.</li>
	<li><strong>Datos Técnicos:</strong> Dirección IP, tipo de navegador, sistema operativo.</li>
	<li><strong>Datos de Uso:</strong> Páginas visitadas, tiempo pasado en el sitio web, interacciones.</li>
	<li><strong>Datos de Transacción:</strong> Detalles de pago (procesados de forma segura por procesadores de pago de terceros).</li>
</ul>
<p><strong>No</strong> recopilamos datos sensibles como creencias religiosas, opiniones políticas o datos biométricos.</p>

<h2>4. Cómo Usamos Sus Datos</h2>
<p>Utilizamos los datos personales para los siguientes propósitos:</p>
<ul>
	<li><strong>Provisión del Servicio:</strong> Asistencia con verificaciones de visa y servicios relacionados con solicitudes.</li>
	<li><strong>Atención al Cliente:</strong> Responder a consultas y proporcionar asistencia.</li>
	<li><strong>Cumplimiento Legal:</strong> Cumplir con requisitos regulatorios y de mantenimiento de registros.</li>
	<li><strong>Marketing:</strong> Envío de actualizaciones y ofertas (solo con su consentimiento).</li>
	<li><strong>Seguridad:</strong> Prevención de fraude y garantía de operación segura del sitio web.</li>
</ul>
<p><strong>No</strong> vendemos ni alquilamos sus datos personales a terceros.</p>

<h2>5. Base Legal para el Procesamiento (GDPR)</h2>
<p>Bajo el GDPR, podemos procesar sus datos basándonos en:</p>
<ul>
	<li><strong>Consentimiento</strong> — Cuando usted acepta explícitamente nuestro procesamiento.</li>
	<li><strong>Necesidad Contractual</strong> — Para proporcionar los servicios que solicita.</li>
	<li><strong>Obligación Legal</strong> — Cuando sea requerido por ley o autoridades regulatorias.</li>
	<li><strong>Interés Legítimo</strong> — Para seguridad, mejora y prevención de fraude.</li>
</ul>

<h2>6. Cuánto Tiempo Conservamos Sus Datos</h2>
<p>Retenemos los datos personales solo el tiempo necesario para cumplir con obligaciones legales, proporcionar servicios, garantizar la seguridad y prevenir el fraude. Después de este período, los datos se eliminan de forma segura o se anonimizan.</p>

<h2>7. Sus Derechos Bajo el GDPR</h2>
<p>Dependiendo de su ubicación, puede tener los siguientes derechos:</p>
<ul>
	<li>Acceso a sus datos personales.</li>
	<li>Corrección de información inexacta o incompleta.</li>
	<li>Eliminación (derecho al olvido).</li>
	<li>Restricción del procesamiento.</li>
	<li>Objeción al procesamiento.</li>
	<li>Portabilidad de datos.</li>
</ul>
<p>Para ejercer cualquiera de estos derechos, contacte: <strong>support@3i-visa.org</strong>.</p>

<h2>8. Cookies y Tecnologías de Seguimiento</h2>
<p>Utilizamos cookies para mejorar la experiencia del usuario, analizar el tráfico y mejorar el rendimiento del servicio. Puede deshabilitar las cookies en la configuración de su navegador, pero algunas funciones pueden no funcionar correctamente.</p>

<h2>9. Servicios de Terceros</h2>
<p>Podemos compartir datos con terceros de confianza que respaldan nuestras operaciones, incluyendo:</p>
<ul>
	<li><strong>Google Analytics</strong> — Análisis del sitio web.</li>
	<li><strong>Meta/Facebook Ads</strong> — Herramientas de marketing y remarketing.</li>
	<li><strong>Procesadores de Pago</strong> — Manejo seguro de pagos.</li>
</ul>
<p>Estos proveedores administran los datos de acuerdo con sus propias políticas de privacidad.</p>

<h2>10. Seguridad de Datos</h2>
<p>Empleamos medidas de seguridad estándar de la industria para proteger sus datos contra acceso no autorizado, alteración o divulgación. Sin embargo, ninguna transmisión en línea está completamente libre de riesgos.</p>

<h2>11. Cambios a Esta Política de Privacidad</h2>
<p>Podemos actualizar esta Política de Privacidad ocasionalmente. Cualquier cambio se publicará en esta página y entrará en vigor inmediatamente después de su publicación.</p>

