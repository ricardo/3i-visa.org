<h2>3. Quais Dados Coletamos</h2>
<p>Dependendo de sua interação com nosso site, podemos coletar os seguintes dados pessoais:</p>
<ul>
	<li><strong>Informações de Identidade:</strong> Nome, endereço de e-mail, número de telefone.</li>
	<li><strong>Dados Técnicos:</strong> Endereço IP, tipo de navegador, sistema operacional.</li>
	<li><strong>Dados de Uso:</strong> Páginas visitadas, tempo gasto no site, interações.</li>
	<li><strong>Dados de Transação:</strong> Detalhes de pagamento (processados de forma segura por processadores de pagamento terceirizados).</li>
</ul>
<p><strong>Não</strong> coletamos dados sensíveis como crenças religiosas, opiniões políticas ou dados biométricos.</p>

<h2>4. Como Usamos Seus Dados</h2>
<p>Usamos dados pessoais para os seguintes propósitos:</p>
<ul>
	<li><strong>Provisão do Serviço:</strong> Assistência com verificações de visto e serviços relacionados à solicitação.</li>
	<li><strong>Atendimento ao Cliente:</strong> Responder a consultas e fornecer assistência.</li>
	<li><strong>Conformidade Legal:</strong> Cumprir requisitos regulatórios e de manutenção de registros.</li>
	<li><strong>Marketing:</strong> Envio de atualizações e ofertas (apenas com seu consentimento).</li>
	<li><strong>Segurança:</strong> Prevenção de fraude e garantia de operação segura do site.</li>
</ul>
<p><strong>Não</strong> vendemos ou alugamos seus dados pessoais a terceiros.</p>

<h2>5. Base Legal para Processamento (GDPR)</h2>
<p>Sob o GDPR, podemos processar seus dados com base em:</p>
<ul>
	<li><strong>Consentimento</strong> — Quando você concorda explicitamente com nosso processamento.</li>
	<li><strong>Necessidade Contratual</strong> — Para fornecer os serviços que você solicita.</li>
	<li><strong>Obrigação Legal</strong> — Quando exigido por lei ou autoridades reguladoras.</li>
	<li><strong>Interesse Legítimo</strong> — Para segurança, melhoria e prevenção de fraude.</li>
</ul>

<h2>6. Por Quanto Tempo Mantemos Seus Dados</h2>
<p>Retemos dados pessoais apenas pelo tempo necessário para cumprir obrigações legais, fornecer serviços, garantir segurança e prevenir fraude. Após esse período, os dados são excluídos de forma segura ou anonimizados.</p>

<h2>7. Seus Direitos Sob o GDPR</h2>
<p>Dependendo de sua localização, você pode ter os seguintes direitos:</p>
<ul>
	<li>Acesso aos seus dados pessoais.</li>
	<li>Correção de informações imprecisas ou incompletas.</li>
	<li>Exclusão (direito ao esquecimento).</li>
	<li>Restrição de processamento.</li>
	<li>Objeção ao processamento.</li>
	<li>Portabilidade de dados.</li>
</ul>
<p>Para exercer qualquer um desses direitos, entre em contato: <strong>support@3i-visa.org</strong>.</p>

<h2>8. Cookies e Tecnologias de Rastreamento</h2>
<p>Usamos cookies para melhorar a experiência do usuário, analisar o tráfego e aprimorar o desempenho do serviço. Você pode desabilitar os cookies nas configurações do seu navegador, mas alguns recursos podem não funcionar corretamente.</p>

<h2>9. Serviços de Terceiros</h2>
<p>Podemos compartilhar dados com terceiros confiáveis que apoiam nossas operações, incluindo:</p>
<ul>
	<li><strong>Google Analytics</strong> — Análise do site.</li>
	<li><strong>Meta/Facebook Ads</strong> — Ferramentas de marketing e remarketing.</li>
	<li><strong>Processadores de Pagamento</strong> — Manuseio seguro de pagamentos.</li>
</ul>
<p>Esses provedores gerenciam dados de acordo com suas próprias políticas de privacidade.</p>

<h2>10. Segurança de Dados</h2>
<p>Empregamos medidas de segurança padrão da indústria para proteger seus dados contra acesso não autorizado, alteração ou divulgação. No entanto, nenhuma transmissão online é totalmente livre de riscos.</p>

<h2>11. Alterações a Esta Política de Privacidade</h2>
<p>Podemos atualizar esta Política de Privacidade ocasionalmente. Quaisquer alterações serão publicadas nesta página e entrarão em vigor imediatamente após a publicação.</p>

