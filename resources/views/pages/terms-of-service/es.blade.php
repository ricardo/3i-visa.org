<p>En la máxima medida permitida por la ley, no somos responsables de ningún daño directo, indirecto, incidental o consecuente que surja de su uso del Servicio.</p>
<p>Nuestra responsabilidad máxima está limitada a la tarifa de servicio total pagada por usted para la solicitud específica en cuestión.</p>

