<h1>Términos de Servicio</h1>

<p>
	Bienvenido a 3i-visa.org ("nosotros", "nuestro" o "nos"). Estos Términos de Servicio ("Términos") rigen su uso de nuestro sitio web, herramientas y servicios relacionados diseñados para asistir con solicitudes de documentos de viaje (colectivamente, el "Servicio").
</p>

<p>
	Al acceder o usar el Servicio, usted acepta estos Términos así como nuestra Política de Privacidad. Si no está de acuerdo, debe descontinuar el uso del Servicio inmediatamente.
</p>

<h2>1. Acerca de Nuestro Servicio</h2>
<p>3i-visa.org es una plataforma en línea que ayuda a los usuarios a preparar y enviar documentación de viaje, incluyendo solicitudes de visa, autorizaciones electrónicas de viaje y formularios relacionados. Brindamos soporte, orientación y manejo de documentos para simplificar el proceso de solicitud.</p>
<p>Somos un servicio privado independiente y no estamos afiliados con ningún gobierno, embajada o autoridad regulatoria. Todas las decisiones finales con respecto a la aprobación o denegación de visas son tomadas únicamente por el gobierno emisor.</p>
<p>Nuestra tarifa de servicio se muestra durante el proceso de pago y no incluye tarifas gubernamentales o consulares a menos que se indique explícitamente. Las tarifas gubernamentales varían según el país y el tipo de documento.</p>

<h2>2. Responsabilidades del Usuario</h2>
<p>Al usar el Servicio, usted acepta:</p>

<ul>
	<li>Proporcionar información completa, precisa y veraz. Los datos incorrectos o incompletos pueden resultar en retrasos o denegación de su solicitud.</li>
	<li>Verificar toda la información y documentos proporcionados en su solicitud final una vez recibida de nosotros.</li>
	<li>Asegurar que su pasaporte y documentos requeridos cumplan con los criterios establecidos por la autoridad emisora.</li>
	<li>Cumplir con las leyes y regulaciones de entrada tanto de su destino como de cualquier país de tránsito.</li>
</ul>

<h2>3. Elegibilidad</h2>
<p>Para usar el Servicio, debe tener al menos 18 años de edad o tener permiso de un padre o tutor legal. También debe proporcionar un método de pago válido y usar el Servicio solo para fines legales.</p>

<h2>4. Pagos y Reembolsos</h2>
<p>Las tarifas de servicio se cobran en el momento de la solicitud. Todos los pagos se procesan de forma segura a través de proveedores de pago de terceros.</p>
<p>Las tarifas gubernamentales, cuando correspondan, son independientes de nuestra tarifa de servicio a menos que se especifique lo contrario.</p>

<h3>Política de Reembolso</h3>
<p>Se pueden otorgar reembolsos si:</p>
<ul>
	<li>Su solicitud aún no ha sido procesada por nuestro equipo; o</li>
	<li>Se cometió un error por nuestra parte al manejar su solicitud.</li>
</ul>

<p>Los reembolsos no están disponibles si:</p>
<ul>
	<li>Su solicitud ya ha sido enviada a un sistema gubernamental; o</li>
	<li>La autoridad emisora rechaza o retrasa su solicitud por cualquier motivo.</li>
</ul>

<h2>5. Uso del Servicio</h2>
<p>Al usar el Servicio, usted acepta recibir comunicaciones esenciales relacionadas con su solicitud, incluidas actualizaciones por correo electrónico y notificaciones de estado. Estos mensajes son necesarios para el funcionamiento adecuado del Servicio.</p>

<h3>Actividades Prohibidas</h3>
<p>Usted no puede:</p>
<ul>
	<li>Enviar información falsa, engañosa o fraudulenta.</li>
	<li>Intentar interferir o eludir las características de seguridad del sitio web.</li>
	<li>Usar el Servicio para facilitar viajes o entrada ilegal a cualquier país.</li>
</ul>

<h2>6. Limitaciones del Servicio</h2>
<p>Si bien buscamos proporcionar orientación precisa y asistencia oportuna, no controlamos las decisiones gubernamentales. La aprobación no está garantizada.</p>
<p>Los tiempos de procesamiento mostrados en el sitio web son estimaciones y pueden variar según la autoridad emisora.</p>

