<h1>Política de Reembolso</h1>
<p><strong>Fecha de Vigencia:</strong> 10 de diciembre de 2025</p>

<h2>1. Introducción</h2>
<p>
	Nuestro objetivo es proporcionar un proceso de reembolso claro, justo y transparente. Esta Política de Reembolso explica las condiciones bajo las cuales se pueden emitir reembolsos por los servicios adquiridos a través de nuestra plataforma.
</p>

<h2>2. Derecho a Solicitar un Reembolso</h2>
<ul>
	<li>Puedes solicitar un reembolso dentro de los 14 días posteriores a la realización de tu pedido.</li>
	<li>Las solicitudes de reembolso deben enviarse contactándonos en <a href="mailto:support@3i-visa.org">support@3i-visa.org</a>.</li>
</ul>

<h2>3. Elegibilidad para Reembolso</h2>
<p>Los reembolsos pueden otorgarse bajo las siguientes condiciones:</p>

<h3>Reembolso Completo – Antes de Comenzar el Procesamiento</h3>
<p>
	Si cancelas tu solicitud antes de que nuestro equipo comience el procesamiento, eres elegible para un reembolso completo de la tarifa del servicio.
</p>

<h3>Reembolso Parcial – Después de Haber Comenzado el Procesamiento</h3>
<p>
	Si el procesamiento ya ha comenzado, se puede emitir un reembolso parcial del 50% cuando la solicitud no puede completarse debido a factores fuera de tu control. Si tu solicitud requiere correcciones o información adicional, reprocesaremos tu solicitud sin costo adicional en lugar de emitir un reembolso.
</p>

<h3>Sin Reembolso – Después de Completar el Procesamiento</h3>
<p>
	Una vez que tu solicitud se haya completado y la documentación correspondiente te haya sido entregada, no se pueden proporcionar reembolsos, ya que el servicio ya se ha cumplido.
</p>

<h2>4. Tiempo de Procesamiento del Reembolso</h2>
<p>
	Los reembolsos aprobados se emitirán al método de pago original dentro de 5 a 10 días hábiles, dependiendo de tu institución financiera.
</p>

<h2>5. Situaciones No Reembolsables</h2>
<p>No se otorgarán reembolsos en las siguientes situaciones:</p>
<ul>
	<li>El servicio ha sido completado y entregado en su totalidad.</li>
	<li>Retrasos o decisiones tomadas por autoridades gubernamentales o de inmigración.</li>
	<li>Falta de información precisa o documentación requerida.</li>
</ul>

<h2>6. Disputas y Prevención de Contracargos</h2>
<p>
	Si encuentras algún problema con tu pedido, por favor contáctanos primero en
	<a href="mailto:support@3i-visa.org">support@3i-visa.org</a>
	antes de presentar un contracargo. Muchos problemas pueden resolverse rápidamente con nuestro equipo de soporte.
</p>

<h2>7. Cambios a Esta Política</h2>
<p>
	Podemos actualizar esta Política de Reembolso en cualquier momento. Cualquier modificación entra en vigencia inmediatamente después de ser publicada en esta página.
</p>
